function summaryInit(currentUe) {
  const summary = document.getElementById("summary-items");
  summary.innerHTML = "";

  const summaryTopics = summaries["ue" + currentUe];

  summaryTopics.forEach((element) => {
    const a = createAnchor(element);

    a.appendChild(createLi(element));

    summary.appendChild(a);
  });
}

function createAnchor(anchorElement) {
  const a = document.createElement("a");
  a.classList.add("summary-button");
  a.classList.add(anchorElement.bold && "font-bold");

  a.href = "#" + anchorElement.href;

  return a;
}

function createLi(anchorElement) {
  const li = document.createElement("li");
  li.classList.add("mb-3");
  li.classList.add(anchorElement.ident == 1 ? "ms-2" : "ms-0");

  li.innerHTML = anchorElement.title;

  return li;
}

function openSummary() {
  document.getElementById("summary").classList.add("w-full");
}

function closeSummary() {
  document.getElementById("summary").classList.remove("w-full");
}

function expandTopic(ref, id) {
  const isChecked = ref.checked;

  const panel = document.getElementById(id);
  if (isChecked) {
    panel.classList.add("h-auto");
    panel.classList.add("flex");
  } else {
    panel.classList.remove("h-auto");
    panel.classList.remove("flex");
  }
}
